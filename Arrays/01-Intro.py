# Arrays - Lists

array = ["JavaScript", "Python", 21, [15,12,7.5], True, True]

print(array)
# Print Python
print(array[1])
# Print Last One
print(array[-1])
# Print from first until 3rd // The last one is how many items
print(array[0:3])
print(array[:3])
#Print from the second to the end
print(array[2:])
# Print Inner Array ->12
print(array[3][1])